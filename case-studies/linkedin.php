            <div class="probleemstelling">
                <h1><strong>probleemstelling:</strong></h1>
                <p><strong>Achtergrond:</strong> Voor een schoolopdracht moesten we een enquête maken met PHP die vacatures toont op basis van gebruikersantwoorden. Dit project was bedoeld om ons te leren omgaan met gegevensverwerking.</p> 
                <p><strong>Uitdaging:</strong> We moesten een systeem ontwikkelen dat
                gebruikersgegevens veilig verzamelt en verwerkt door middel van een enquête. en die vacatures toont die aansluiten bij de antwoorden van de gebruiker door middel van een zoek- en filtersysteem.</p>
            </div>

            <!-- Aanpak -->
            <div class="aanpak">
                <h1><strong>Aanpak:</strong></h1>
                <p><strong>Project Plan:</strong> We begonnen met het opstellen van een project plan om het project gestructureerd aan te pakken.</p>
                <p><strong>Ontwerp:</strong> Voor het ontwerp van de enquête en de vacaturepagina's heb ik gebruikgemaakt van Figma. Dit hielp ons om een helder ontwerp te creëren dat gebruiksvriendelijk was.</p>
                <p><strong>Ontwikkeling:</strong> Na goedkeuring van het ontwerp begonnen we met de ontwikkeling. Met behulp van PHP bouwden we de backend die gebruikersinvoer verwerkt en op basis van die input vacatures toont.</p>
            </div>

            <!-- Resultaten -->
            <div class="resultaten">
                <h1><strong>Resultaten:</strong></h1>
                <p><strong>Eindproduct:</strong> Het eindproduct is een volledig functionele enquêteapplicatie die gebruikersinvoer verwerkt en op basis van de antwoorden passende vacatures toont. De backend, gebouwd met PHP, werkt naar behoren en voldoet aan alle functionele eisen die in het plan van aanpak waren opgenomen.</p>
                <p><strong>Impact:</strong> Het systeem werd goed ontvangen door de “klant” en voldeed aan de belangrijkste functionele vereisten. De applicatie slaagde erin om relevante vacatures aan te bevelen op basis van de gebruikersantwoorden, wat de oorspronkelijke doelstelling van het project was.</p>
                <p><strong>Feedback:</strong> De enige feedback die we ontvingen, had betrekking op de frontend. Hoewel het systeem functioneel was, voldeed het ontwerp niet volledig aan de oorspronkelijk gemaakte schetsen in Figma. Ook was de applicatie helaas niet responsive, wat betekende dat deze niet optimaal werkte op verschillende apparaten en schermformaten.</p>
            </div>

            <!-- Reflectie -->
            <div class="reflectie">
                <h1><strong>Reflectie:</strong></h1>
                <p><strong>Geleerde lessen:</strong> Ik heb geleerd hoe ik met PHP gegevens kan versturen en verwerken via de POST-methode, wat cruciaal was voor het krijgen van de resulaten.</p>
                <p><strong>Toekomstige verbeteringen:</strong> In de toekomst zou ik meer aandacht besteden aan het maken van een responsief ontwerp en ervoor zorgen dat het frontend-ontwerp beter overeenkomt met de Schetsen.</p>
